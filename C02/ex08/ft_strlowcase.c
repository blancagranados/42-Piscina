#include <stdio.h>
#include <unistd.h>

char	*ft_strlowcase(char *str)
{
	int i;

	i = 0;

	while (str[i])
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
		{
			str[i] = str[i] + 32;
		}
		i++;
	}
	return (str);
}

int		main(void)
{
	char a[] = "adAleee";

	printf("%s\n", ft_strlowcase(a));
	return (0);
}
