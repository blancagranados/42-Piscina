#include <stdio.h>
#include <unistd.h>

char	*ft_strcpy(char *dest, char *src)
{
	int i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

int		main(void)
{
	char str1[] = "Sample string";
	char str2[40];
	ft_strcpy(str2, str1);
	printf("str1: %s\nstr2: %s\n", str1, str2);
	return (0);
}